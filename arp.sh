# ARP stands for Address Resolution Protocol
# The arp command resolves a device's ip address on a local network into its mac address


# The arp cache is where out computer saves the associations it's learned about a network

# using the command without arguments displays the arp cache.
# no flag or -e displays it in linux style, -a displays it in bsd style
sudo arp -e


# to scan all devices on the network you can use a command called arp-scan with --localnet
sudo arp-scan --localnet




















